Description Ex3: 
Write a script that takes an integer as an argument and returns "Even" for even numbers or "Odd" for odd numbers.
Solution Ex3:
a=$1
if (( a % 2 == 0 ))
then
    echo "Even"
else
    echo "Odd"
fi
